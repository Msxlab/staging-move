/**
 * LocateFlow — CSS design-token source of truth + emitter.
 *
 * STEP 1 of the design-system foundation: this module is the *single*
 * structured source for every CSS custom property that previously lived
 * hand-synced across five files:
 *
 *   1. apps/web/src/styles/globals.css      (`:root,.dark` + `.light` + shadcn HSL)
 *   2. apps/admin/src/app/globals.css       (`:root,.dark` + `.light` + shadcn HSL)
 *   3. apps/web/src/styles/aurora.css        (`.lf-aurora` `--au-*` primitives)
 *   4. apps/admin/src/app/aurora.css         (`.adm-aurora` `--au-*` primitives)
 *
 * `scripts/emit-design-tokens.ts` consumes the maps below and writes the
 * `_tokens.generated.css` / `_aurora-tokens.generated.css` partials that
 * those files now `@import`. The drift-guard test re-runs the emitter in
 * memory and asserts the committed output is byte-identical, so a hand
 * edit to any generated file fails CI.
 *
 * PURE REFACTOR: every value below is copied verbatim from the current
 * hand-written CSS. This is *not* the place to change a colour — that is
 * Step 2. The TS runtime token model (`./design-tokens`) keeps its own
 * exports for the mobile app and is intentionally left untouched here;
 * the two models overlap conceptually but the CSS copies carried small
 * historical drifts (e.g. `--tone-foil-bg` alpha) that must be preserved
 * exactly to guarantee zero visual change.
 */

// ────────────────────────────────────────────────────────────────────
// Types
// ────────────────────────────────────────────────────────────────────

/** An ordered list of CSS custom-property declarations. `comment` rows
 *  emit a `/* … *\/` line; `blank` rows emit an empty line; `var` rows
 *  emit `  --name: value;` aligned to the block's declared padding. */
export type TokenRow =
  | { kind: "var"; name: string; value: string }
  | { kind: "comment"; text: string }
  | { kind: "blank" }
  /** Pre-formatted raw line emitted verbatim (used for the admin aurora
   *  single-line tone triplets that pack three decls per source line). */
  | { kind: "raw"; text: string };

export interface CssBlock {
  /** Selector list, e.g. `":root,\n.dark"` or `".light .lf-aurora"`. */
  selector: string;
  rows: TokenRow[];
}

export interface CssLayer {
  /** When set, wraps `blocks` in `@layer <name> { … }`. */
  layer?: string;
  blocks: CssBlock[];
}

// ────────────────────────────────────────────────────────────────────
// Emitter
// ────────────────────────────────────────────────────────────────────

const v = (name: string, value: string): TokenRow => ({ kind: "var", name, value });
const c = (text: string): TokenRow => ({ kind: "comment", text });
const raw = (text: string): TokenRow => ({ kind: "raw", text });
const blank: TokenRow = { kind: "blank" };

export interface EmitOptions {
  /**
   * Minimum column width for the `--name:` portion of each declaration.
   * The actual width per block is `max(pad, longest `--name:` in the block
   * + 1)` so every value is preceded by at least one space and the colons
   * align within a block.
   */
  pad: number;
}

/**
 * Emit a list of layers/blocks to a CSS partial string with the leading
 * "generated" banner. This is the shared rendering engine; `renderTarget`
 * is a thin wrapper that pairs a layer set with its output path and pad.
 */
export function emitCssVars(layers: CssLayer[], options: EmitOptions): string {
  const out: string[] = [GENERATED_BANNER, ""];

  for (const layer of layers) {
    if (layer.layer) {
      out.push(`@layer ${layer.layer} {`);
      const parts = layer.blocks.map((block) =>
        block.selector === "__PLAN__"
          ? renderPlanRows(block.rows, "  ")
          : renderBlock(block, "  ", options.pad),
      );
      out.push(parts.join("\n\n"));
      out.push("}");
    } else {
      const parts = layer.blocks.map((b) => renderBlock(b, "", options.pad));
      out.push(parts.join("\n\n"));
    }
    out.push("");
  }

  // Collapse trailing blanks into a single terminal newline.
  return out.join("\n").replace(/\n+$/, "\n");
}

