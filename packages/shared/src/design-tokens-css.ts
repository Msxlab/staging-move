/**
 * Render one target to its final CSS string. The plan-class pseudo-block
 * (`selector === "__PLAN__"`) is emitted as bare lines inside the layer,
 * matching the original `globals.css` shape.
 */
export function renderTarget(target: EmitTarget): string {
  return emitCssVars(target.layers, { pad: target.pad });
}

/**
 * Render JUST the shadcn `@layer base` block body (no generated banner) for
 * a given app. This is the canonical text that globals.css inlines, so the
 * drift test can assert the inline copy still matches the model.
 */
export function renderShadcnLayer(app: "web" | "admin"): string {
  const outPath =
    app === "web"
      ? "apps/web/src/styles/_tokens-shadcn.generated.css"
      : "apps/admin/src/app/_tokens-shadcn.generated.css";
  const target = emitTargets.find((t) => t.outPath === outPath);
  if (!target) throw new Error(`no shadcn target for ${app}`);
  // Strip the banner + the leading blank line that follows it.
  return renderTarget(target).replace(`${GENERATED_BANNER}\n\n`, "").trimEnd();
}

function renderBlock(block: CssBlock, indent: string, pad: number): string {
  const lines: string[] = [];
  // Selector may contain newlines (`:root,\n.dark`). The first line takes
  // the block indent; continuation lines keep their authored indentation.
  const renderedSelector = block.selector
    .split("\n")
    .map((s, i) => (i === 0 ? `${indent}${s}` : s))
    .join("\n");
  lines.push(`${renderedSelector} {`);

  // Align declarations within the block: width is at least `pad`, but grows
  // so the longest `--name:` is still followed by a space (never jammed).
  const longestDecl = block.rows.reduce((max, row) => {
    if (row.kind !== "var") return max;
    return Math.max(max, `--${row.name}:`.length + 1);
  }, 0);
  const width = Math.max(pad, longestDecl);

  for (const row of block.rows) {
    if (row.kind === "blank") {
      lines.push("");
    } else if (row.kind === "comment") {
      lines.push(`${indent}  /* ${row.text} */`);
    } else if (row.kind === "raw") {
      lines.push(`${indent}  ${row.text}`);
    } else {
      const decl = `--${row.name}:`;
      lines.push(`${indent}  ${decl.padEnd(width, " ")}${row.value};`);
    }
  }
  lines.push(`${indent}}`);
  return lines.join("\n");
}

function renderPlanRows(rows: TokenRow[], indent: string): string {
  const lines: string[] = [];
  for (const row of rows) {
    if (row.kind === "blank") {
      lines.push("");
    } else if (row.kind === "comment") {
      lines.push(`${indent}/* ${row.text} */`);
    } else if (row.kind === "raw") {
      lines.push(`${indent}${row.text}`);
    }
  }
  return lines.join("\n");
}
